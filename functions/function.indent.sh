#!/usr/bin/env bash

# ==============================================================================
# ------------------------------------------------------------------------------
function indent() {
    c='s/^/       /'
    case $(uname) in
        Darwin) sed -l "$c";;
        *)      sed -u "$c";;
    esac
}
# ==============================================================================


# ==============================================================================
if [ "${0}" = "${BASH_SOURCE}" ];then
    # direct call to file
    indent $@
fi # else file is included from another script
# ==============================================================================

#EOF
