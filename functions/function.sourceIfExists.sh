#!/usr/bin/env bash

# ==============================================================================
# ------------------------------------------------------------------------------
function sourceIfExists() {
    sSourceFile="$1"
    if [ -f "$sSourceFile" ]; then
        source "${sSourceFile}"
    fi
}
# ==============================================================================


# ==============================================================================
if [ "${0}" = "${BASH_SOURCE}" ];then
    # direct call to file
    sourceIfExists $@
fi # else file is included from another script
# ==============================================================================

#EOF
