#!/usr/bin/env bash

# ==============================================================================
# Output all given Messages to STDERR
# ------------------------------------------------------------------------------
function outputErrorMessages() {
    echo -e "\nErrors occurred:\n\n ${@}" >&2
}
# ==============================================================================


# ==============================================================================
if [ "$0" = "$BASH_SOURCE" ];then
    # direct call to file
    outputErrorMessages "$@"
fi # else file is included from another script
# ==============================================================================

#EOF
