#!/usr/bin/env bash

# ==============================================================================
function isInstalled() {
# ------------------------------------------------------------------------------
    return $(command -v "${1}" >/dev/null 2>&1);
}
# ==============================================================================


# ==============================================================================
if [ "${0}" = "${BASH_SOURCE}" ];then
    # direct call to file
    isInstalled $@
fi # else file is included from another script
# ==============================================================================

#EOF
