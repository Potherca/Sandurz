#!/usr/bin/env bash

# ==============================================================================
function isMacOS() {
# ------------------------------------------------------------------------------
    [ "$(uname -s)" == "Darwin" ] && return 0 || return 1;
}
# ==============================================================================


# ==============================================================================
if [ "${0}" = "${BASH_SOURCE}" ];then
    # direct call to file
    isMacOS
fi # else file is included from another script
# ==============================================================================

#EOF
