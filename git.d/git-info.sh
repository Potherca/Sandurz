#!/usr/bin/env bash

# =============================================================================='
echo '# =============================================================================='
echo ' User Information'
echo '#-------------------------------------------------------------------------------'
echo -n ' User Name  : ' && git config user.name
echo -n ' User Email : ' && git config user.email
echo '# =============================================================================='
# =============================================================================='

echo ''
# =============================================================================='
echo '# =============================================================================='
echo 'URL(s):'
echo '#-------------------------------------------------------------------------------'
git remote -v
echo '# =============================================================================='
# =============================================================================='
echo ''
# =============================================================================='
echo '# =============================================================================='
echo 'Branche(s):'
echo '# =============================================================================='
echo ' Local :'
echo '#-------------------------------------------------------------------------------'
git branch --list
echo ''
echo '#-------------------------------------------------------------------------------'
echo ' Remotes :'
#git branch --remotes
for sRemote in $(git remote show);do
echo '#-------------------------------------------------------------------------------'
    echo "$(git remote show ${sRemote})"
done
echo '# =============================================================================='
# =============================================================================='
echo ''
# =============================================================================='
echo '# =============================================================================='
echo ' Additional Information'
echo '#-------------------------------------------------------------------------------'
git log --max-count=1 --pretty=format:'Last Changed Author: %an%nLast Channged Rev: %h%nLast Changed Date:  %aD '
echo "Commit Count: $(git shortlog | grep -E '^[ ]+\w+' | wc -l)"
echo '# =============================================================================='
# =============================================================================='
echo ''
# =============================================================================='

#EOF
